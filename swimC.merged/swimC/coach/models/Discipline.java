package swimC.coach.models;

public class Discipline {
   private int id;
   private String name;
  
   public int getID() {
      return id;
   }
   public String getName() {
      return name;
   }
   public void setID(int i) {
      this.id = i;
   }
   public void setName(String i) {
      this.name = i;
   }
   
}